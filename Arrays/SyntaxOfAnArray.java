public class SyntaxOfAnArray {
    public static void main(String[] args) {
        int[] arr1 = { 1, 2, 3, 4, 5 };
        System.out.println(arr1[3]);

        int[] arr2 = new int[3];
        arr2[0] = 5;
        arr2[1] = 4;
        arr2[2] = 3;
        System.out.println(arr2[0]);
        System.out.println(arr2[1]);
        System.out.println(arr2[2]);

    }
}
